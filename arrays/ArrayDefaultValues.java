public class ArrayDefaultValues {
    public static void main(String[]args){
        int[] numbers=new int[3];
        double[] prices = new double[3];
         boolean[] status = new boolean[3];
        char[] grades = new char[3];
        String[] names = new String[3];

        System.out.println("Default int value: " + numbers[0]);
        System.out.println("Default double value: " + prices[0]);
        System.out.println("Default boolean value: " + status[0]);
        System.out.println("Default char value: " + grades[0]);
        System.out.println("Default String value: " + names[0]);

    }
    
}
